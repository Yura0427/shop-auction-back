export default function pobedovColorTranslator(color: string) {
  color = color.toLowerCase();
  if (color === 'бордовый') return 'Бордовий';
  if (color === 'голубой') return 'Голубий';
  if (color === 'желтый') return 'Жовтий';
  if (color === 'пудра') return 'Пудра';
  if (color === 'светло-серый') return 'Світло-сірий';
  if (color === 'темно-серый') return 'Темно-сірий';
  if (color === 'navy') return 'Navy';
  if (color === 'антрацит') return 'Антрацит';
  if (color === 'бежевый') return 'Бежевий';
  if (color === 'белый') return 'Білий';
  if (color === 'зеленый') return 'Зелений';
  if (color === 'коричневый') return 'Коричневий';
  if (color === 'красный') return 'Червоний';
  if (color === 'серый') return 'Сірий';
  if (color === 'синий') return 'Синій';
  if (color === 'хаки') return 'Хакі';
  if (color === 'черный') return 'Чорний';
  if (color === 'космос') return 'Космос';
  if (color === 'розовый') return 'Рожевий';
  if (color === 'оливковый') return 'Оливковий';
}
